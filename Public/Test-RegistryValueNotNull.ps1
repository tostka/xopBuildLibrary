function Test-RegistryValueNotNull {
                [OutputType('bool')]
                [CmdletBinding()]
                PARAM(
                    [Parameter(Mandatory=$true)]
                        [ValidateNotNullOrEmpty()]
                        [string]$Key,
                    [Parameter(Mandatory=$true)]
                        [ValidateNotNullOrEmpty()]
                        [string]$Value
                )
                $ErrorActionPreference = 'Stop'
                if (($regVal = Get-ItemProperty -Path $Key -Name $Value -ErrorAction Ignore) -and $regVal.($Value)) {
                    $true
                }
            }
